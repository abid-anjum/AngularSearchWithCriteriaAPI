package com.aisc.angular.search.model;

import javax.persistence.*;

@Entity
@DiscriminatorValue("regularworker")
public class Regular_Worker extends Worker {

	@Column(name = "salary")
	private float salary;

	@Column(name = "bonus")
	private int bonus;

	public float getSalary() {
		return salary;
	}

	public void setSalary(float salary) {
		this.salary = salary;
	}

	public int getBonus() {
		return bonus;
	}

	public void setBonus(int bonus) {
		this.bonus = bonus;
	}

}